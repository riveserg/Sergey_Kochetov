package ru.job4j.max;

/**
 * Max.
 *
 * @author Kochetov
 * @version $Id$
 * @since 0.1
 */
public class Max {
    /**
     * Method return maximum of 2 numbers.
     * @param first - first number
     * @param second - second number
     * @return - maximum
     */
    public int max(int first, int second) {
        return first > second ? first : second;
    }
}
